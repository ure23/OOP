class Student {
    private int Student_Id;
    private String Name;

    public void setStudentId(int Student_Id) {
        this.Student_Id = Student_Id;
    }

    public int getStudentId() {
        return Student_Id;
    }

    public void setName(String Name) {
        this.Name = Name;
    }

    public String getName() {
        return Name;
    }
}
